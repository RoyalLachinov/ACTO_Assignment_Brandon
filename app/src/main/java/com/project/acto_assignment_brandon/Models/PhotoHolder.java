package com.project.acto_assignment_brandon.Models;

public class PhotoHolder {

    private String title;
    private String url;

    public PhotoHolder(String title, String url) {
        this.title = title;
        this.url = url;
    }

    public String getTitle() {
        return title;
    }

    public String getUrl() {
        return url;
    }
}
